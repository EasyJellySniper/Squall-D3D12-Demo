struct ViewPort
{
    /// <summary>
    /// view port data
    /// </summary>
    public float TopLeftX;
    public float TopLeftY;
    public float Width;
    public float Height;
    public float MinDepth;
    public float MaxDepth;
}

struct RawRect
{
    /// <summary>
    /// raw rect data
    /// </summary>
    public int left;
    public int top;
    public int right;
    public int bottom;
}
